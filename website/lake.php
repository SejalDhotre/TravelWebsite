Lakes are still bodies of water that are formed in the basins of land. A basin is a dip in the surface of the earth that is formed due to various geological reasons. When rainwater is collected in a basin, or a river flows into a basin, it fills that basin with water and that basin is then called a lake.<br>
So how the basins are formed that are converted into the lakes. 
There are many reasons behind the formation of basins. Some of the reasons are earthquakes, movement of glaciers, and landslides.
